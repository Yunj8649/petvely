package egovframework.example.cart.service;

public class TAB_CART_VO {

	private int prCd = 0;
	private String accId = "";
	private int crCd = 0;
	private String crClr = "";
	private String crOpt = "";
	private int crQty = 0;
	public int getPrCd() {
		return prCd;
	}
	public void setPrCd(int prCd) {
		this.prCd = prCd;
	}
	public String getAccId() {
		return accId;
	}
	public void setAccId(String accId) {
		this.accId = accId;
	}
	public int getCrCd() {
		return crCd;
	}
	public void setCrCd(int crCd) {
		this.crCd = crCd;
	}
	public String getCrClr() {
		return crClr;
	}
	public void setCrClr(String crClr) {
		this.crClr = crClr;
	}
	public String getCrOpt() {
		return crOpt;
	}
	public void setCrOpt(String crOpt) {
		this.crOpt = crOpt;
	}
	public int getCrQty() {
		return crQty;
	}
	public void setCrQty(int crQty) {
		this.crQty = crQty;
	}
	
	
}
